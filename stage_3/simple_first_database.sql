--------------------------------------------------------

  CREATE OR REPLACE FUNCTION "MMARKIEW"."GET_SUM_OF_CLIENT_ORDERS" (i_client_id in clients.client_id%type) return orders.value%type is
  val orders.value%type;
begin
  select sum(value) into val from orders where client_id = i_client_id;

  if val is null then
    val := 0;
  end if;
 
  return val;
end;

